    /**
     * instancia de un objeto Asignatura
     *
     * @return La asignatura instanciada
     */
    private Asignatura crearAsignatura() {
        return new Asignatura(nombreTex.getText(), profesorTex.getText(), aulaTex.getText(), Double.parseDouble(horaTex.getText()), colores[menuColores.getSelectedIndex()], contacTex.getText());
    }

    /**
     * Metodo para inicializar los campos de texto con los datos de la
     * asignatura
     */
    private void rellenarCampos(Asignatura a) {
        int colorNum = 0;
        nombreTex.setText(a.getNombre());
        profesorTex.setText(a.getProfesor());
        aulaTex.setText(a.getAula());
        contacTex.setText(a.getContacto());
        horaTex.setText(Double.toString(a.getHorasSema()));
        for (int i = 0; i < colores.length; i++) {
            if (a.getColor().equals(colores[i])) {
                colorNum = i;
            }
        }
        menuColores.setSelectedIndex(colorNum);
    }

    /**
     * Metodo que comprueba si estan todos los datos bien rellenados
     *
     * @return boolean de si todo esta bien rellenado
     */
    private boolean todoCorrecto() {
        boolean r = true;
        if (nombreTex.getText().equalsIgnoreCase("") || profesorTex.getText().equalsIgnoreCase("")
                || aulaTex.getText().equalsIgnoreCase("") || horaTex.getText().equalsIgnoreCase("")
                || contacTex.getText().equalsIgnoreCase("")) {
            r = false;
        }
        return r;
    }

