    /**
     * Boton crear Asignatura controla que todo este bien y envia el objeto
     *
     * @param evt
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (!todoCorrecto()) {
                JOptionPane.showMessageDialog(null, "DEBE RELLENAR TODOS LOS CAMPOS", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                principal.addAsignatura(crearAsignatura(), modifica);
                principal.setVisible(true);
                this.dispose();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "EL CAMPO HORAS DEBE SER UN NUMERO", "ERROR", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton2ActionPerformed
